import "dotenv/config";
import { Pool } from "pg";

const pool = new Pool({
  connectionString: process.env.DATABASE_URL,
  ssl: { rejectUnauthorized: false },
});

async function main() {
  try {
    const result = await pool.query(
      "SELECT * FROM password_reset_tokens WHERE user_id = 10 ORDER BY created_at DESC LIMIT 5"
    );
    console.log("Recent tokens for user 10:", result.rows);
  } catch (err) {
    console.error("DB Error:", (err as any).message);
  } finally {
    await pool.end();
    process.exit(0);
  }
}

main();
